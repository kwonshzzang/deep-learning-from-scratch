
a = [1, 2, 3, 4, 5]  # 리스트 생성
print(a)             # 리스트의 내용 출력

print(len(a))        # 리스트의 길이 출력

print(a[0])          # 첫 원소에 접근

print(a[4])          # 다섯 번째 원소에 접근

a[4] = 99            # 값 대입
print(a)

print(a[0:2])        # 인덱스 0부터 2까지 얻기(2번째는 포함되지 않는다)

print(a[1:])         # 인덱스 1부터 끝까지 얻기

print(a[:3])         # 처음부터 인덱스 3까지 얻기(3번째는 포함되지 않는다)

print(a[:-1])        # 처음부터 마지막 원소의 1개 앞까지 얻기

print(a[:-2])        # 처음부터 마지막 원소의 2개 앞까지 얻기